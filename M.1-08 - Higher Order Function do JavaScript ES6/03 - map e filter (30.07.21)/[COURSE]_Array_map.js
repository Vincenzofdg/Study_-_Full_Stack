// é uma função de objeto tipo array, que serve pra criar um novo array com o resultado da função que for passado como parametro


// Exemplo 01
const persons = [
  { firstName: 'Maria', lastName: 'Ferreira' },
  { firstName: 'João', lastName: 'Silva' },
  { firstName: 'Antonio', lastName: 'Cabral' },
];

// Utilizando o for:
const fullName = [];

for (let index = 0; index < persons.length; index += 1) {
  fullName.push(`${persons[index].firstName} ${persons[index].lastName}`);
}

console.log(fullName);

// Utilizando map:
const fullNames = persons.map((person) => `${person.firstName} ${person.lastName}`);

console.log(fullNames);


// Exemplo 02:
const numbers = [1, 2, 3, 4, -5];

const negativeNumbers = numbers.map((number) => ((number > 0) ? number * (-1) : number));

console.log(negativeNumbers); // [ -1, -2, -3, -4, -5 ]
console.log(numbers); // [ 1, 2, 3, 4, -5 ]


// Exemplo 03: Unir dois arrays para se tornar apenas um
const products = ['Arroz', 'Feijão', 'Alface', 'Tomate'];
const prices = [2.99, 3.99, 1.5, 2];

const updateProducts = (listProducts, listPrices) => listProducts.map((product, index) => (
  { [product]: listPrices[index] }
));

const listProducts = updateProducts(products, prices);
console.log(listProducts);


// Exemplo 04: O forEach muda o original enquando o map preserva
const numeros = [1, 2, 3, 4, 5, 6];
console.log(numeros.map((numero) => numero * 2)); // Retorno: [2, 4, 6, 8, 10, 12]

const paresMenoresQueCinco = [];
numeros.forEach((numero) => {
  if (numero < 5 && numero % 2 === 0) {
    paresMenoresQueCinco.push(numero);
  }
});
console.log(paresMenoresQueCinco); // Retorno: [2, 4]


